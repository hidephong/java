    private void jtf_UsernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtf_UsernameFocusLost
        // TODO add your handling code here:
        Employee ep =null;
        ep= em.getEmployeeByUsername(jtf_Username.getText());
        if(ep==null){
             chkuser.setText("Username is available!");
             btnSave.setEnabled(true);
        }
        else {chkuser.setText("Username isn't available!");}
    }//GEN-LAST:event_jtf_UsernameFocusLost

    private void jtf_EmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtf_EmailFocusLost
        // TODO add your handling code here:
        
         boolean bl= vp.validateEmail(jtf_Email.getText().toString());
         if(bl){
             chkemail.setText("Email correct!");
             btnSave.setEnabled(true);
        }
        else {chkemail.setText("Email incorrect!");
         btnSave.setEnabled(false);
        }
    }//GEN-LAST:event_jtf_EmailFocusLost

