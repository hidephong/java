    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

                Employee emp = new Employee();
                 emp.setUsername(jtf_Username.getText());
                 emp.setEmployeeName(jtf_Fullname.getText());
                 emp.setPassword(jPass1.getPassword().toString());
                 int rid = rm.get_RoleIDByName(jCbbRole.getSelectedItem().toString());
                 emp.setRoleID(rid);
                 emp.setWorkExperience(Integer.parseInt(jtf_WorkExperience.getText()));
                 emp.setJoiningDate(jDCDateJoin.getDate());
                 emp.setPhoneNumber(jtf_Phonenumber.getText().toString());
                 emp.setEmail(jtf_Email.getText().toString());
                 emp.setWorkItemID(pm.getWorkItemID(cbProjOnDep.getSelectedItem().toString()));
                 emp.setAddress(jtf_Address.getText());
                    if(jCbbGen.getSelectedIndex()==0)
                     { emp.setGender("male"); }
                        else { emp.setGender("female");}
                
                    if(jChkStatus.isSelected())
                     {
                          emp.setStatus("enabled");}
                        else
                     {
                        emp.setStatus("disabled"); }
                boolean br =   em.add(emp);
             if(br){ JOptionPane.showMessageDialog(this,"Add new employee success!","Alert",1);}
             else { JOptionPane.showMessageDialog(this,"Add new employee not success!","Alert",1);}

    }//GEN-LAST:event_btnSaveActionPerformed

