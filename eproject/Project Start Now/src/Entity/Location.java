package Entity;

/**
 *
 * @author nhahv
 */
public class Location {
    private int LocationID;
    //private String LocationName;
    private String LocationAddress;
    private float LocationAllowance;
    private String LocationStatus;

    public Location() {
    }
    
    
   
    public Location(int lID,String LAdd, float lAl, String stt){
        this.LocationID = lID;
        this.LocationAddress = LAdd;
        this.LocationAllowance = lAl;
        this.LocationStatus = stt;
                
        
    }
    
       /**
     * @return the LocationID
     */
    public int getLocationID() {
        return LocationID;
    }

    /**
     * @param LocationID the LocationID to set
     */
    public void setLocationID(int LocationID) {
        this.LocationID = LocationID;
    }

    /**
     * @return the LocationAddress
     */
    public String getLocationAddress() {
        return LocationAddress;
    }

    /**
     * @param LocationAddress the LocationAddress to set
     */
    public void setLocationAddress(String LocationAddress) {
        this.LocationAddress = LocationAddress;
    }

    /**
     * @return the LocationAllowance
     */
    public float getLocationAllowance() {
        return LocationAllowance;
    }

    /**
     * @param LocationAllowance the LocationAllowance to set
     */
    public void setLocationAllowance(float LocationAllowance) {
        this.LocationAllowance = LocationAllowance;
    }

    /**
     * @return the LocationStatus
     */
    public String getLocationStatus() {
        return LocationStatus;
    }

    /**
     * @param LocationStatus the LocationStatus to set
     */
    public void setLocationStatus(String LocationStatus) {
        this.LocationStatus = LocationStatus;
    }

//  

}
