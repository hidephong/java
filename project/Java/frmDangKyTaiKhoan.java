    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username=txttaikhoan.getText();
        String password=new String(txtmatkhau.getPassword());
        String xnPassword=new String(txtxacnhanmatkhau.getPassword());
        String str="dangky@"+username+"@"+password;
        if(!password.equals(xnPassword)){
            JOptionPane.showMessageDialog(null, "Mật khẩu và xác nhận mật khẩu không trùng nhau!","Lỗi",JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                String data="";
                socket=new Socket(frmFTPClient.HOST,frmFTPClient.PORT);
                in=new Scanner(socket.getInputStream());
                out=new  PrintWriter(socket.getOutputStream(),true);
                out.println(str);
                data=in.nextLine().trim();
                if(data.equals("OK")){// Đăng nhập thành công
                    JOptionPane.showMessageDialog(null, "Thông báo","Đăng ký thành công!",JOptionPane.INFORMATION_MESSAGE);
                    xoa();
                }else{//Đăng nhập thất bại
                    JOptionPane.showMessageDialog(null, "Đăng ký thất bại do lỗi đường truyền!","Lỗi",JOptionPane.ERROR_MESSAGE);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e.toString(),"Lỗi",JOptionPane.ERROR_MESSAGE);
            }
        }       
    }//GEN-LAST:event_jButton1ActionPerformed


