    private void jBT_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBT_saveActionPerformed
        float f;

        if(jftPostAllowance.getText().length()>12){
            jLb_Message.setText("Error: Allowance damn huge!");
            return;
        }
        if(jtfRole.getText().isEmpty() || jftPostAllowance.getText().isEmpty()){
            jLb_Message.setText("Error: Role and Allowance can not be empty !");
            return;
        }
        try {
            f = Float.parseFloat(jftPostAllowance.getText());
        } catch (Exception e) {
            jLb_Message.setText("Error: Allowance must be real");
            return;
        }
        if(!jcbStatus.isSelected()){
            int choice = JOptionPane.showConfirmDialog(this, "Do you want to enable this post now ?", "Notice !", JOptionPane.YES_NO_OPTION);
            if(choice ==0 ){
                jcbStatus.setSelected(true);
            }
        }

        for (Post post : pVec) {
            if(post.getRole().equalsIgnoreCase(jtfRole.getText())){
                jLb_Message.setText("Error: Role alailable already !");
                return;
        }
        }
        
        Post p = new Post();
        p.setRole(jtfRole.getText());
        p.setPostAllowance(f);
        p.setBStatus(jcbStatus.isSelected());    
        
        boolean add = pData.addAPost(p);
        if(add){
             JOptionPane.showMessageDialog(null, "Add post successful!");
             this.dispose();
            }else{
             JOptionPane.showMessageDialog(null, "Add post failed!");
            }
        
    }//GEN-LAST:event_jBT_saveActionPerformed

