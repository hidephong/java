package Entity;

import java.util.Date;

/**
 *
 * @author PHUONGPHUONG
 */
public class WorkItem {
    private int WorkItemID;
    private int DepartmentID;
    private int ProjectID;
    private Date StartDate;
    private Date EndDate;
    private String Status;

    private String DepartmentName;
    private String ProjectName;
    private String LocationAdress;
    private int LocationID;


    /**
     * @return the WorkItemID
     */
    public int getWorkItemID() {
        return WorkItemID;
    }

    /**
     * @param WorkItemID the WorkItemID to set
     */
    public void setWorkItemID(int WorkItemID) {
        this.WorkItemID = WorkItemID;
    }

    /**
     * @return the DepartmentID
     */
    public int getDepartmentID() {
        return DepartmentID;
    }

    /**
     * @param DepartmentID the DepartmentID to set
     */
    public void setDepartmentID(int DepartmentID) {
        this.DepartmentID = DepartmentID;
    }

    /**
     * @return the ProjectID
     */
    public int getProjectID() {
        return ProjectID;
    }

    /**
     * @param ProjectID the ProjectID to set
     */
    public void setProjectID(int ProjectID) {
        this.ProjectID = ProjectID;
    }

    /**
     * @return the StartDate
     */
    public Date getStartDate() {
        return StartDate;
    }

    /**
     * @param StartDate the StartDate to set
     */
    public void setStartDate(Date StartDate) {
        this.StartDate = StartDate;
    }

    /**
     * @return the EndDate
     */
    public Date getEndDate() {
        return EndDate;
    }

    /**
     * @param EndDate the EndDate to set
     */
    public void setEndDate(Date EndDate) {
        this.EndDate = EndDate;
    }

    /**
     * @return the Status
     */
    public String getStatus() {
        return Status;
    }

    /**
     * @param Status the Status to set
     */
    public void setStatus(String Status) {
        this.Status = Status;
    }

    /**
     * @return the DepartmentName
     */
    public String getDepartmentName() {
        return DepartmentName;
    }

    /**
     * @param DepartmentName the DepartmentName to set
     */
    public void setDepartmentName(String DepartmentName) {
        this.DepartmentName = DepartmentName;
    }

    /**
     * @return the ProjectName
     */
    public String getProjectName() {
        return ProjectName;
    }

    /**
     * @param ProjectName the ProjectName to set
     */
    public void setProjectName(String ProjectName) {
        this.ProjectName = ProjectName;
    }

    /**
     * @return the LocationAdress
     */
    public String getLocationAdress() {
        return LocationAdress;
    }

    /**
     * @param LocationAdress the LocationAdress to set
     */
    public void setLocationAdress(String LocationAdress) {
        this.LocationAdress = LocationAdress;
    }

    /**
     * @return the LocationID
     */
    public int getLocationID() {
        return LocationID;
    }

    /**
     * @param LocationID the LocationID to set
     */
    public void setLocationID(int LocationID) {
        this.LocationID = LocationID;
    }
}
